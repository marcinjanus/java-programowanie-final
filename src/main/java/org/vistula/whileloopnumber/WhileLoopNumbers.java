package org.vistula.whileloopnumber;

public class WhileLoopNumbers {
    //pętla while, która wypisze na ekranie wszystkie liczby od 10 do 20 włącznie.
    public static void main(String[] args) {
        int i = 10;
        while (i <= 20) {
            System.out.println(i);
            i=i+1;
        }
    }
}
